package logg;

public class ens {
private int ens_id;
private String ens_nom;
private String ens_pnom;
private String ens_nutili;
private String ens_pass;
private String ens_mati;
public ens() {
	super();
}
public ens(int ens_id, String ens_nom, String ens_pnom, String ens_nutili, String ens_pass, String ens_mati) {
	super();
	this.ens_id = ens_id;
	this.ens_nom = ens_nom;
	this.ens_pnom = ens_pnom;
	this.ens_nutili = ens_nutili;
	this.ens_pass = ens_pass;
	this.ens_mati = ens_mati;
}
public int getEns_id() {
	return ens_id;
}
public void setEns_id(int ens_id) {
	this.ens_id = ens_id;
}
public String getEns_nom() {
	return ens_nom;
}
public void setEns_nom(String ens_nom) {
	this.ens_nom = ens_nom;
}
public String getEns_pnom() {
	return ens_pnom;
}
public void setEns_pnom(String ens_pnom) {
	this.ens_pnom = ens_pnom;
}
public String getEns_nutili() {
	return ens_nutili;
}
public void setEns_nutili(String ens_nutili) {
	this.ens_nutili = ens_nutili;
}
public String getEns_pass() {
	return ens_pass;
}
public void setEns_pass(String ens_pass) {
	this.ens_pass = ens_pass;
}
public String getEns_mati() {
	return ens_mati;
}
public void setEns_mati(String ens_mati) {
	this.ens_mati = ens_mati;
}
	
	
	
	
}
