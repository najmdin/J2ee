package logg;

public class etu {
private int idetu;
private String pnetu;
private String netu;
private int nbr;
private String cls;
private String nuti;
private String pss;
public etu() {
	super();
}
public etu(int idetu, String pnetu, String netu, int nbr, String cls, String nuti, String pss) {
	super();
	this.idetu = idetu;
	this.pnetu = pnetu;
	this.netu = netu;
	this.nbr = nbr;
	this.cls = cls;
	this.nuti = nuti;
	this.pss = pss;
}
public int getIdetu() {
	return idetu;
}
public void setIdetu(int idetu) {
	this.idetu = idetu;
}
public String getPnetu() {
	return pnetu;
}
public void setPnetu(String pnetu) {
	this.pnetu = pnetu;
}
public String getNetu() {
	return netu;
}
public void setNetu(String netu) {
	this.netu = netu;
}
public int getNbr() {
	return nbr;
}
public void setNbr(int nbr) {
	this.nbr = nbr;
}
public String getCls() {
	return cls;
}
public void setCls(String cls) {
	this.cls = cls;
}
public String getNuti() {
	return nuti;
}
public void setNuti(String nuti) {
	this.nuti = nuti;
}
public String getPss() {
	return pss;
}
public void setPss(String pss) {
	this.pss = pss;
}
}
